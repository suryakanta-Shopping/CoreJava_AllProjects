package java.switchs.statement;

public class Test01 {

	public static void main(String[] args) {

		// declare a variable for switch exp
		int num = 20;

		switch (num) {
		// Case statements
		case 10:
			System.out.println("10");
			break;
		case 20:
			System.out.println("20");
			break;
		case 30:
			System.out.println("30");
			break;
		// default case statement
		default:
			System.out.println("Not the Value");
		}

	}

}
